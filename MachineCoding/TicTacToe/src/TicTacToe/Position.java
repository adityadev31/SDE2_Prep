package TicTacToe;

public class Position {
    private final int x;
    private final int y;

    // constructor
    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // getter & setter
    public int getX() { return this.x; }
    public int getY() { return this.y; }
}
