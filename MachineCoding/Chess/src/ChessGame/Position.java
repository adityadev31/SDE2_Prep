public class Position {
    private int x;
    private int y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
    public void setX(int x1) {
        this.x = x1;
    }
    public void setY(int y1) {
        this.y = y1;
    }
}
